/**
 * 列举rust所有基本类型
 */
fn main() {
    // 整数
    let number: i32 = 1;
    // 浮点数
    let float: f32 = 1.0;
    // 布尔值
    let boolean: bool = true;
    // 字符
    let character: char = 'a';
    // 字符串
    let string: &str = "hello";
    // 数组
    let array: [i32; 3] = [1, 2, 3];
    // 元组
    let tuple: (i32, f32, bool) = (1, 1.0, true);
}
